const sourceTexts = new WeakMap();
const sourceAttributes = new WeakMap();

export function getLanguageName(code, displayLanguage = 'en') {
  if (displayLanguage === 'ro') return code === 'ro' ? 'Română' : 'Engleză';
  return code === 'ro' ? 'Romanian' : 'English';
}

export function t(key, language = 'en', params = {}) {
  const table = ui[language] || ui.en;
  const value = table[key] ?? ui.en[key] ?? key;
  return typeof value === 'function' ? value(params) : value;
}

export function translateInterface(root, language = 'en') {
  document.documentElement.lang = language;
  const showText = globalThis.NodeFilter?.SHOW_TEXT ?? 4;
  const walker = document.createTreeWalker(root || document.body, showText);
  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);

  textNodes.forEach(node => {
    const parentTag = node.parentElement?.tagName;
    if (['SCRIPT', 'STYLE', 'CODE', 'PRE', 'TEXTAREA'].includes(parentTag)) return;
    const current = node.nodeValue;
    if (!current || !current.trim()) return;
    if (!sourceTexts.has(node)) sourceTexts.set(node, current);
    const source = sourceTexts.get(node);
    const trimmed = source.trim();
    const translated = language === 'ro' ? exactRomanian.get(trimmed) : null;
    if (translated) {
      const leading = source.match(/^\s*/)?.[0] || '';
      const trailing = source.match(/\s*$/)?.[0] || '';
      const nextValue = `${leading}${translated}${trailing}`;
      if (node.nodeValue !== nextValue) node.nodeValue = nextValue;
    } else if (node.nodeValue !== source) {
      node.nodeValue = source;
    }
  });

  (root || document).querySelectorAll?.('[placeholder], [aria-label], [title]').forEach(element => {
    if (!sourceAttributes.has(element)) sourceAttributes.set(element, {});
    const saved = sourceAttributes.get(element);
    ['placeholder', 'aria-label', 'title'].forEach(attribute => {
      if (!element.hasAttribute(attribute)) return;
      if (!(attribute in saved)) saved[attribute] = element.getAttribute(attribute);
      const source = saved[attribute];
      const translated = language === 'ro' ? exactRomanian.get(source) : null;
      const nextValue = translated || source;
      if (element.getAttribute(attribute) !== nextValue) element.setAttribute(attribute, nextValue);
    });
  });
}
